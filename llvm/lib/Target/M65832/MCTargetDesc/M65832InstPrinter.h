#ifndef LLVM_LIB_TARGET_M65832_MCTARGETDESC_M65832INSTPRINTER_H
#define LLVM_LIB_TARGET_M65832_MCTARGETDESC_M65832INSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class M65832InstPrinter : public MCInstPrinter {
public:
  M65832InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                     const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;
  void printRegName(raw_ostream &O, MCRegister Reg) override;

  // Autogenerated by tblgen
  std::pair<const char *, uint64_t> getMnemonic(const MCInst &MI) const override;
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &O);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &O);
  static const char *getRegisterName(MCRegister Reg);
  
  // Use const MCInst & for TableGen compatibility
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printDPOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printMemOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printBranchTarget(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printCondCode(const MCInst *MI, unsigned OpNo, raw_ostream &O);
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_M65832_MCTARGETDESC_M65832INSTPRINTER_H
